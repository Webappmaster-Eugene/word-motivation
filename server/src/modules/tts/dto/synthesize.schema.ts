import { z } from 'zod';

/**
 * Ограничение на длину текста — страхует от spam'а и от случайного кейса,
 * когда LLM-ответ оказался гигантским. Silero на CPU синтезирует ~0.5 сек
 * аудио в секунду; 400 символов = ~30 сек речи = ~60 сек CPU. Ставим порог 400,
 * что покрывает даже длинные подсказки FSM ("В слове собака спряталась буква С...").
 */
export const synthesizeSchema = z.object({
  text: z.string().trim().min(1, 'Текст не может быть пустым').max(400),
  /**
   * Голос Silero. Необязателен — при отсутствии используется
   * `TTS_DEFAULT_VOICE` из env.
   */
  voice: z.enum(['xenia', 'kseniya', 'baya', 'aidar', 'eugene']).optional(),
  /**
   * Скорость произношения (0.5 — очень медленно, 1.5 — быстро). Позволяет
   * подстроить под контекст: буква медленнее (~0.85), приветствие в обычном
   * темпе. Silero параметр `rate` не поддерживает напрямую — используем
   * `resample` на стороне worker'а (меняем частоту дискретизации → изменение
   * длительности без pitch-shift).
   */
  rate: z.coerce.number().min(0.5).max(1.5).optional(),
});

export type SynthesizeDto = z.infer<typeof synthesizeSchema>;
